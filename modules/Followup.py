def normalize(text):
    return text.lower().strip()

def should_check_group(group_name, symptoms_dict):
    if group_name == "pregnancy":
        return symptoms_dict.get("pregnancy_status", 0) == 1
    return True

PRIORITY_ORDER = [
    "snake bite", "animal bite", "burn", "pesticide poisoning", "drug exposure",
    "drowning", "cardiac", "stroke", "pregnancy", "seizure", "dengue",
    "heat stroke", "wound", "abdominal pain", "hurts to breath",
    "abnormal involuntary movements", "diarrhea", "mensuration","headache",
    "weakness", "dizziness"
]

def detect_followup_categories(symptoms_dict, FOLLOWUP_GROUPS, lang,detected_special=None):
    active = set()
    if detected_special and detected_special.get("found"):
        cond = detected_special["condition"].lower()
        if "snake" in cond:
            return ["snake bite"]
        elif "bite" in cond or "scratch" in cond:
            return ["animal bite"]
        elif "burn" in cond or "scald" in cond:
            return ["burn"]
        elif "poison" in cond or "pesticide" in cond:
            return ["pesticide poisoning"]
        elif "drug" in cond or "inject" in cond:
            return ["drug exposure"]
        elif "drown" in cond:
            return ["drowning"]

    normalized_symptoms = {k.lower().strip(): v for k, v in symptoms_dict.items()}
    trigger_key = "triggers_en" if lang == "English" else "triggers_bn"

    for group_name, group_data in FOLLOWUP_GROUPS.items():
        if group_name == "pregnancy" and symptoms_dict.get("ispregnant", 0) != 1:
            continue
        triggers = group_data.get(trigger_key, [])
        for trigger in triggers:
            t = trigger.lower().strip()
            if normalized_symptoms.get(t, 0) == 1:
                active.add(group_name)
                break
            for sym, val in normalized_symptoms.items():
                if val == 1 and (t in sym or sym in t):
                    active.add(group_name)
                    break
            else:
                continue
            break

   
    sorted_active = sorted(active, key=lambda x: PRIORITY_ORDER.index(x) if x in PRIORITY_ORDER else 99)
    return sorted_active[:1]